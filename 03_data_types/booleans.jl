# Boolean values and operations
is_true = true
is_false = false

println("Is true? ", is_true)
println("Is false? ", is_false)

# Logical operations
and_result = is_true && is_false
or_result = is_true || is_false
not_result = !is_true

println("AND operation: ", and_result)
println("OR operation: ", or_result)
println("NOT operation: ", not_result)
