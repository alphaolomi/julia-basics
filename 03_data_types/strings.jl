# Working with strings
hello = "Hello"
world = "World"

# String concatenation
greeting = hello * ", " * world * "!"
println(greeting)

# String interpolation
name = "Julia"
personal_greeting = "Hello, $name!"
println(personal_greeting)

# Multiline strings
multiline = """
This is a
multiline string
in Julia.
"""
println(multiline)
