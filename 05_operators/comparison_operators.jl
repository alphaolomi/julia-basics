# Comparison Operators
x = 5
y = 10

# Equality
println("x == y: ", x == y)

# Inequality
println("x != y: ", x != y)

# Greater than
println("x > y: ", x > y)

# Less than
println("x < y: ", x < y)

# Greater than or equal to
println("x >= y: ", x >= y)

# Less than or equal to
println("x <= y: ", x <= y)
