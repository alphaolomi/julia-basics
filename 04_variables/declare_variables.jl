# Declaring variables
x = 10              # Integer
y = 3.14            # Float
name = "Julia"      # String
is_programming = true  # Boolean

println("x: ", x)
println("y: ", y)
println("name: ", name)
println("is_programming: ", is_programming)

# Changing variable values
x = 15
println("New value of x: ", x)

# Julia is dynamically typed
y = "now I'm a string"
println("y: ", y)


# Single line, multiple variable declaration
println("\nSingle line, multiple variable declaration")
a, b, c = 1, 2, 3
println("a: ", a)
println("b: ", b)
println("c: ", c)
